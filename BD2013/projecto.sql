CREATE TABLE Alimento (
	nomeA VARCHAR(80),
	vegetariano BOOLEAN,
	PRIMARY KEY(nomeA)
);

CREATE TABLE Simples (
	nomeA VARCHAR(80),
	calgramas INTEGER,
	tipo VARCHAR(255) NOT NULL,
	FOREIGN KEY(nomeA) REFERENCES Alimento(nomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(nomeA)
);

CREATE TABLE Agregado (
	nomeA VARCHAR(80),
	calorias INTEGER NOT NULL,
	FOREIGN KEY (nomeA) REFERENCES Alimento(nomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(nomeA)
);

CREATE TABLE Composto (
	nomeAgg VARCHAR(80),
	nomeS VARCHAR(80),
	quantidade INTEGER NOT NULL,
	FOREIGN KEY (nomeAgg) REFERENCES Agregado(nomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (nomeS) REFERENCES Simples(NomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(nomeAgg,nomeS)
);

CREATE TABLE Prato (
	nomeA VARCHAR(80),
	preco INTEGER NOT NULL,
	FOREIGN KEY(nomeA) REFERENCES Alimento(nomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(nomeA)
);

CREATE TABLE Restaurante (
	nomeR VARCHAR(80),
	PRIMARY KEY(nomeR)
);

CREATE TABLE Data (
	dia INTEGER,
	mes VARCHAR(45),
	ano INTEGER,
	PRIMARY KEY(dia,mes,ano)
);

 CREATE TABLE Disponivel (
	nomeA VARCHAR(80),
	nomeR VARCHAR(80),
	dia INTEGER,
	mes VARCHAR(45),
	ano INTEGER,
	FOREIGN KEY(nomeA) REFERENCES Prato(nomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY(nomeR)  REFERENCES Restaurante(nomeR) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY(dia,mes,ano) REFERENCES Data(dia,mes,ano) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(nomeA,nomeR,dia,mes,ano)
);

CREATE TABLE Venda (
	nomeA VARCHAR(80),
	dia INTEGER,
	mes varchar(45),
	ano INTEGER,
	nomeR VARCHAR(80),
	num INTEGER,
	FOREIGN KEY(nomeA) REFERENCES Prato(nomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY(dia,mes,ano) REFERENCES Data(dia,mes,ano) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY(nomeR) REFERENCES Restaurante(nomeR) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(nomeA,dia,mes,ano,nomeR)
);


CREATE TABLE Cliente (
	email VARCHAR(255) NOT NULL UNIQUE,
	PRIMARY KEY(email)
);

CREATE TABLE Encomenda (
	email VARCHAR(255),
	nEnc INTEGER,
	precoTotal FLOAT(5,2),
	desconto INTEGER,
	nomeR VARCHAR(255),
	dia INTEGER,
	mes VARCHAR(45),
	ano INTEGER,
	FOREIGN KEY(email) REFERENCES Cliente(email) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY(nomeR) REFERENCES Restaurante(nomeR) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY(dia,mes,ano) REFERENCES Data(dia,mes,ano) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(email,nEnc)
);

CREATE TABLE RegistoEnc (
	email VARCHAR(255),
	nEnc INTEGER,
	nomeA VARCHAR(80),
	FOREIGN KEY(email,nEnc) REFERENCES Encomenda(email,nEnc) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY(nomeA) REFERENCES Prato(nomeA) ON DELETE CASCADE ON UPDATE CASCADE,
	PRIMARY KEY(email,nEnc,nomeA)
);

